    /*
    Formato de dataBase.db
    
    String Nombre Completo
    int puntos
    long Fecha de creacion
    String username
    String password
    */
    
    private boolean autenticar(String username, String password) throws IOException{
        boolean autenticado = false;

        //Setea el archivo del que vamos a leer
        creacionDeUsuarios.dataBase = new RandomAccessFile("dataBase.db", "rw");
        
        //Asegura el puntero
        creacionDeUsuarios.dataBase.seek(0);
        
        //Recorrido de archivo
        while (creacionDeUsuarios.dataBase.getFilePointer() < creacionDeUsuarios.dataBase.length()){
            creacionDeUsuarios.dataBase.readUTF(); //Mueve el puntero la cantidad de Bytes que abarque el Nombre Completo del user
            creacionDeUsuarios.dataBase.skipBytes(12); //Skip 4 (int puntos) + 8 (long fecha) = 12 bytes
            
            //Extrae los datos correctos que usara de referencia
            String userRef = (String)creacionDeUsuarios.dataBase.readUTF();
            String passRef = (String)creacionDeUsuarios.dataBase.readUTF();
            
            //Verifica que el username y passsword ingresados concuerden
            
            if ((userRef).equals(username) && (passRef).equals(password)){
                autenticado = true;
            }
        }
        
        return autenticado;
    }
    
